class BfCalculator {
  List<int> setBenchmarks({
    required bool male,
    required int ageGroupIndex,
    required int height,
  }) {
    int min, max, percentMax, heightIndex = height >= 58 ? height - 58 : 0;

    min = heightWeightTable[heightIndex][8];
    max = heightWeightTable[heightIndex]
        [male ? ageGroupIndex : ageGroupIndex + 4];
    percentMax = percentTable[male ? ageGroupIndex : ageGroupIndex + 4];

    return [min, max, percentMax];
  }

  int getNewBfPercent({
    required bool male,
    required int weight,
    required double cirValue,
  }) {
    double weightSolution = (male ? 0.12 : 0.015) * weight;
    double circSolution = (male ? 1.99 : 1.27) * cirValue;
    return ((male ? -26.97 : -9.15) - weightSolution + circSolution).round();
  }

  int getBfPercent({
    required bool male,
    required double height,
    required double cirValue,
  }) {
    double safeHeight = height < 58
        ? 58
        : height > 80
            ? 80
            : height;
    if (male) {
      if (cirValue < 10.5) {
        return 0;
      }
      return maleBfTable[maleCirValues.indexOf(cirValue)]
          [heights.indexOf(safeHeight)];
    } else {
      if (cirValue < 34.5) {
        return 0;
      }
      return femaleBfTable[femaleCirValues.indexOf(cirValue)]
          [heights.indexOf(safeHeight)];
    }
  }

